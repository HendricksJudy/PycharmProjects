# 数学相关函数

# 获取一个数的绝对值
print(abs(-99.99))

# 求和 从strat 开始从左向右 对 iterable 中的项求和并返回总计值
print(sum([1, 2, 3]))

# 获取最大值
print(max(1, 2, 3))

# 获取最小值
print(min(6, 7, -6, 90, -12))

# 幂运算
print(pow(2, 3))

# 对一个数进行四舍五入 奇进偶退1.5 ==> 2  2.5 ==> 2
r = round(3.1415926)
r = round(3.1415926, 4)  # 小数点保留几位
print(r)